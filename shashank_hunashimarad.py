import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from matplotlib.backends.backend_pdf import PdfPages
import seaborn as sns

dataset=pd.read_csv("/content/drive/My Drive/DS_DATESET.csv")
dataset.head()

pdf=PdfPages('vizual-output.pdf')

#2-a) no. of students applied to different technologies
a=dataset['Areas of interest'].value_counts().plot(x='No of students',y='Different Technologies',figsize=(20,10),fontsize=15,color='Blue',kind='barh')
