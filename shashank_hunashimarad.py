#2-b) The number of students applied for Data Science who knew ‘’Python” and who didn’t.
b = dataset[['Areas of interest','Programming Language Known other than Java (one major)']]
bi = b.groupby(['Areas of interest'])
bii = bi.get_group('Data Science ')
lst = list(bii['Programming Language Known other than Java (one major)'])
x=lst
lst1=[]
for i in range(0,len(lst)):
    if (x[i]=="Python"):
        lst1.append("YES")
    else:
        lst1.append("NO")
lst2 = pd.DataFrame({"Python":lst1})
be=lst2['Python'].value_counts().plot(figsize=(20,10),fontsize=15,color='pink',kind='bar')
be.set_ylabel('No. of Students in Data Science Who Know Python',fontsize=25)
be.set_xlabel("Students who know Python",fontsize=25)
plt.title('Students in Data Science Who Know Python',fontsize=25)
pdf.savefig()

